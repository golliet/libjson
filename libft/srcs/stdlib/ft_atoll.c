#include "libft.h"

long long		ft_atoll(const char *str)
{
	long long	nb;
	int			i;
	int			neg;

	nb = 0;
	i = 0;
	neg = 1;
	while (ft_isspace(str[i]))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		nb *= 10;
		nb += (str[i] - 48);
		i++;
	}
	return (nb * neg);
}
